function [c, ceq, DC, DCeq] = confun(y)
% Nonlinear inequality constraints
c = [
%    -x(1) + 0.031*x(6).^-1 + 0.335*x(3).^-1*x(5).^2 + 2.46*x(10)
    log(0.031*exp(-y(6)-y(1)) + 0.335*exp(-y(3) + 2*y(5) - y(1)) +...
    2.46*exp(y(10) - y(1))),
%    -x(2) + 0.194*x(3).^1.5*x(6).^0.5*x(8).^0.5 + 45.2*x(6)
    log(0.194*exp(1.5*y(3) + 0.5*y(6) + 0.5*y(8) - y(2)) + 45.2*exp(y(6)-y(2))),
%    -6.91e+04*x(3).^-0.5*x(6).^0.5*x(9) + x(7)
    log(1/(6.91e4)) + y(7) + 0.5*y(3) - 0.5*y(6) - y(9),
%    -x(10) + 0.074*x(7).^-0.2
    -y(10) + log(0.074) - 0.2*y(7),
%    -0.615*x(5)*x(6)*x(9).^2 + x(8)
    y(8) + log(1/0.615) - y(5) - y(6) - 2*y(9),
    %-446*x(6) + x(8)
    y(8) + log(1/446) - y(6),
    %-x(8) + 4.94e+03 + x(2)
    log(4.94e3*exp(-y(8)) + exp(y(2)-y(8))),
    ];

ceq = [
%      -x(4) + 0.615*x(1)*x(6)*x(9).^2
      -y(4) + log(0.615) + y(1) + y(6) + 2*y(9)
      ];
end
